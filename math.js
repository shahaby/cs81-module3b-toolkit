// Function to double a number
function double(n) {
    return n * 2;
}

// Function to square a number
function square(n) {
    return n * n;
}

// Function to check if a number is even
function isEven(n) {
    return n % 2 === 0;
}

// Function to check if a number is odd
function isOdd(n) {
    return n % 2 !== 0;
}

// Function to multiply two numbers
function multiply(a, b) {
    return a * b;
}

